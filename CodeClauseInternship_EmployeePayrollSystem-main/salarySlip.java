    private void GetSlipActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GetSlipActionPerformed
        // TODO add your handling code here:
        String empId = id.getText();
        int intId = Integer.parseInt(empId);
        try {
            Class.forName("com.mysql.jdbc.Driver");
          Connection con ;
         
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/payroll_manage", "root", "sql@Lipton");
            
              PreparedStatement ps = con.prepareStatement("select * from emp where empId = ?");
//              ps.setString(1, intId);
              ps.setInt(1, intId);
              
              ResultSet rs = ps.executeQuery();
              if(rs.next() != false){
                   name.setText(rs.getString("name"));
                   email.setText(rs.getString("email"));
                   
                   int depid = Integer.parseInt(rs.getString("depId"));
                   PreparedStatement ps1 = con.prepareStatement("select * from department where depID = ?");
                   ps1.setInt(1, depid);
                   ResultSet rs1 = ps1.executeQuery();
                   if (rs1.next()) {
                        department.setText(rs1.getString("depName"));
                    }
                   
                   int basicpay = 0;
                   int posid = Integer.parseInt(rs.getString("positionId"));
                   PreparedStatement ps2 = con.prepareStatement("select * from position where posID = ?");
                   ps2.setInt(1, posid);
                   ResultSet rs2 = ps2.executeQuery();
                   if (rs2.next()) {
                        position.setText(rs2.getString("posName"));
                        basicPay.setText(rs2.getString("salary"));
                        basicpay = Integer.parseInt(rs2.getString("salary"));
                   }
                   
                    hours.setText(rs.getString("workingDays"));
                    bonus.setText(rs.getString("bonus"));
                    tax.setText("5%");
                    
                    int workingDays = Integer.parseInt(rs.getString("workingDays"));
                    int bonus = Integer.parseInt(rs.getString("bonus"));

                    // Calculate salary
                    int totalEarnings = workingDays * (basicpay/30) + bonus;
                    int salary = (int) (totalEarnings * 0.95);

                    // Convert salary to String before setting it
                    grossSalary.setText(String.valueOf(salary));
                    
                  

                   
              }
              else{
                  JOptionPane.showMessageDialog(null, "Your Employee Id is invalid");
              }
        } catch (SQLException ex) {
            Logger.getLogger(salarySlip.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(salarySlip.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_GetSlipActionPerformed

